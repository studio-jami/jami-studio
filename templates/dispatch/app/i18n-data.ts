type Messages = typeof enUS;
type PartialMessages = {
  [K in keyof Messages]?: K extends "messaging"
    ? {
        managed?: Partial<Messages["messaging"]["managed"]> &
          Record<string, unknown>;
      } & Record<string, unknown>
    : K extends "dispatch"
      ? {
          nav?: Partial<Messages["dispatch"]["nav"]> & Record<string, unknown>;
          pages?: Partial<Messages["dispatch"]["pages"]> &
            Record<string, unknown>;
        } & Record<string, unknown>
      : Partial<Messages[K]> & Record<string, unknown>;
};

function mergeMessages(overrides: PartialMessages): Messages {
  return {
    root: { ...enUS.root, ...overrides.root },
    routeTitles: { ...enUS.routeTitles, ...overrides.routeTitles },
    settings: { ...enUS.settings, ...overrides.settings },
    integrations: {
      ...enUS.integrations,
      ...overrides.integrations,
      status: {
        ...enUS.integrations.status,
        ...overrides.integrations?.status,
      },
      readiness: {
        ...enUS.integrations.readiness,
        ...overrides.integrations?.readiness,
      },
      grantMode: {
        ...enUS.integrations.grantMode,
        ...overrides.integrations?.grantMode,
      },
      time: {
        ...enUS.integrations.time,
        ...overrides.integrations?.time,
      },
    },
    messaging: {
      ...enUS.messaging,
      ...overrides.messaging,
      managed: {
        ...enUS.messaging.managed,
        ...(
          overrides.messaging as
            | { managed?: Record<string, unknown> }
            | undefined
        )?.managed,
        health: {
          ...enUS.messaging.managed.health,
          ...(
            overrides.messaging as
              | { managed?: { health?: Record<string, string> } }
              | undefined
          )?.managed?.health,
        },
        trust: {
          ...enUS.messaging.managed.trust,
          ...(
            overrides.messaging as
              | { managed?: { trust?: Record<string, string> } }
              | undefined
          )?.managed?.trust,
        },
      },
    },
    dispatch: {
      ...enUS.dispatch,
      ...overrides.dispatch,
      nav: {
        ...enUS.dispatch.nav,
        ...(overrides.dispatch as { nav?: Record<string, string> } | undefined)
          ?.nav,
      },
      pages: {
        ...enUS.dispatch.pages,
        ...(
          overrides.dispatch as { pages?: Record<string, string> } | undefined
        )?.pages,
      },
    },
  };
}


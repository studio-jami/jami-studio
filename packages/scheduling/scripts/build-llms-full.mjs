// Concatenate all docs into a single llms-full.txt bundle for AI consumption.
// Run by `pnpm build` after `tsc`.
import { readFileSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join, relative } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = fileURLToPath(new URL(".", import.meta.url));
const docsDir = join(__dirname, "..", "docs");

function walk(dir, files = []) {
  for (const name of readdirSync(dir)) {
    const full = join(dir, name);
    const st = statSync(full);
    if (st.isDirectory()) walk(full, files);
    else if (name.endsWith(".md") || name === "llms.txt") files.push(full);
  }
  return files;
}

const files = walk(docsDir).sort();
const out = [];
out.push("# @agent-native/scheduling — Full Documentation Bundle");
out.push("");
out.push(
  "Generated by scripts/build-llms-full.mjs. Includes every markdown doc",
);
out.push("and skill. For a curated index, see llms.txt.");
out.push("");
for (const file of files) {
  if (file.endsWith("llms-full.txt")) continue;
  const rel = relative(docsDir, file);
  out.push(`\n---\n## ${rel}\n`);
  out.push(readFileSync(file, "utf8"));
}
writeFileSync(join(docsDir, "llms-full.txt"), out.join("\n"));
console.log(
  `Wrote ${files.length} files → docs/llms-full.txt (${out.join("\n").length} bytes)`,
);
